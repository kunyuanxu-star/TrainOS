/// Global font instance (lazily generated).
static mut FONT_8X16: [u8; 128 * 16] = [0u8; 128 * 16];
static mut FONT_INITIALIZED: bool = false;

/// Get the global 8x16 font, initializing it on first call.
pub fn font_8x16() -> &'static [u8; 128 * 16] {
    unsafe {
        if !FONT_INITIALIZED {
            FONT_8X16 = generate_font_8x16();
            FONT_INITIALIZED = true;
        }
        &FONT_8X16
    }
}

// ── Text Drawing Functions ─────────────────────────────────────────────────

/// Draw text with word wrapping.
/// Returns the position of the next character (next_x, next_y).
pub fn draw_text_wrapped(
    fb: &mut Framebuffer,
    x: u32,
    y: u32,
    max_w: u32,
    text: &str,
    fg: Color,
    bg: Color,
) -> (u32, u32) {
    let font = font_8x16();
    let char_w = 8u32;
    let char_h = 16u32;
    let mut cx = x;
    let mut cy = y;

    for ch in text.chars() {
        if ch == '\n' {
            cx = x;
            cy += char_h;
            continue;
        }
        if cx + char_w > x + max_w && cx > x {
            cx = x;
            cy += char_h;
        }
        if cx + char_w <= x + max_w {
            fb.draw_char(cx, cy, ch, fg, bg, font);
            cx += char_w;
        }
    }
    (cx, cy + char_h)
}

/// Compute the rendered width in pixels of a text string.
pub fn text_width(text: &str) -> u32 {
    (text.chars().filter(|&c| c != '\n').count() as u32) * 8
}

/// Compute the rendered height in pixels of a text string.
pub fn text_height(text: &str) -> u32 {
    let lines = text.chars().filter(|&c| c == '\n').count() + 1;
    (lines as u32) * 16
}

/// Draw text centered within a rectangle.
pub fn draw_text_centered(
    fb: &mut Framebuffer,
    r: &Rect,
    text: &str,
    fg: Color,
    bg: Color,
) {
    let tw = text_width(text);
    let th = text_height(text);
    let x = r.x + (r.width as i32 - tw as i32) / 2;
    let y = r.y + (r.height as i32 - th as i32) / 2;
    draw_text_wrapped(
        fb,
        x.max(0) as u32,
        y.max(0) as u32,
        r.width,
        text,
        fg,
        bg,
    );
}
