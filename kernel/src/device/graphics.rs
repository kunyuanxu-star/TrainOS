/// Color type (RGBA32).
pub type Color = u32;

pub const BLACK: Color      = 0xFF000000;
pub const WHITE: Color      = 0xFFFFFFFF;
pub const RED: Color        = 0xFFFF0000;
pub const GREEN: Color      = 0xFF00FF00;
pub const BLUE: Color       = 0xFF0000FF;
pub const GRAY: Color       = 0xFF808080;
pub const DARK_GRAY: Color  = 0xFF404040;
pub const LIGHT_GRAY: Color = 0xFFC0C0C0;
pub const CYAN: Color       = 0xFF00FFFF;
pub const MAGENTA: Color    = 0xFFFF00FF;
pub const YELLOW: Color     = 0xFFFFFF00;
pub const TRANSPARENT: Color = 0x00000000;
pub const ORANGE: Color     = 0xFFFFA500;
pub const NAVY: Color       = 0xFF000080;
pub const TEAL: Color       = 0xFF008080;
pub const MAROON: Color     = 0xFF800000;
pub const DARK_BG: Color    = 0xFF1E1E2E;
pub const TITLE_BG: Color   = 0xFF2D2D44;
pub const WINDOW_BG: Color  = 0xFFF0F0F0;
pub const DESKTOP_BG: Color = 0xFF2E3440;

/// Create a packed RGBA32 color from components.
pub const fn rgba(r: u8, g: u8, b: u8, a: u8) -> Color {
    ((a as u32) << 24) | ((r as u32) << 16) | ((g as u32) << 8) | (b as u32)
}

/// Extract the red component.
pub fn red(c: Color) -> u8 { (c >> 16) as u8 }
/// Extract the green component.
pub fn green(c: Color) -> u8 { (c >> 8) as u8 }
/// Extract the blue component.
pub fn blue(c: Color) -> u8 { c as u8 }
/// Extract the alpha component.
pub fn alpha(c: Color) -> u8 { (c >> 24) as u8 }

/// Blend two colors with alpha.
pub fn blend(src: Color, dst: Color) -> Color {
    let sa = alpha(src) as u32;
    if sa == 0xFF { return src; }
    if sa == 0 { return dst; }
    let da = alpha(dst) as u32;
    let out_a = sa + da * (255 - sa) / 255;
    let out_r = (red(src) as u32 * sa + red(dst) as u32 * da * (255 - sa) / 255) / out_a;
    let out_g = (green(src) as u32 * sa + green(dst) as u32 * da * (255 - sa) / 255) / out_a;
    let out_b = (blue(src) as u32 * sa + blue(dst) as u32 * da * (255 - sa) / 255) / out_a;
    rgba(out_r as u8, out_g as u8, out_b as u8, out_a as u8)
}

/// Darken a color by a factor (0.0 = black, 1.0 = unchanged).
pub fn darken(c: Color, factor: f32) -> Color {
    let f = factor.max(0.0).min(1.0);
    rgba(
        (red(c) as f32 * f) as u8,
        (green(c) as f32 * f) as u8,
        (blue(c) as f32 * f) as u8,
        alpha(c),
    )
}

/// Lighten a color by a factor (0.0 = unchanged, 1.0 = white).
pub fn lighten(c: Color, factor: f32) -> Color {
    let f = factor.max(0.0).min(1.0);
    rgba(
        (red(c) as f32 + (255 - red(c)) as f32 * f) as u8,
        (green(c) as f32 + (255 - green(c)) as f32 * f) as u8,
        (blue(c) as f32 + (255 - blue(c)) as f32 * f) as u8,
        alpha(c),
    )
}

// ── Geometry Types ─────────────────────────────────────────────────────────

#[derive(Clone, Copy, Debug)]
pub struct Point {
    pub x: i32,
    pub y: i32,
}

impl Point {
    pub const fn new(x: i32, y: i32) -> Self { Point { x, y } }
}

#[derive(Clone, Copy, Debug)]
pub struct Rect {
    pub x: i32,
    pub y: i32,
    pub width: u32,
    pub height: u32,
}

impl Rect {
    pub const fn new(x: i32, y: i32, w: u32, h: u32) -> Self {
        Rect { x, y, width: w, height: h }
    }

    pub fn contains(&self, p: &Point) -> bool {
        p.x >= self.x
            && p.x < (self.x + self.width as i32)
            && p.y >= self.y
            && p.y < (self.y + self.height as i32)
    }

    pub fn intersects(&self, other: &Rect) -> bool {
        let ax2 = self.x + self.width as i32;
        let ay2 = self.y + self.height as i32;
        let bx2 = other.x + other.width as i32;
        let by2 = other.y + other.height as i32;
        !(self.x >= bx2 || ax2 <= other.x || self.y >= by2 || ay2 <= other.y)
    }

    /// Return a new rect inset by dx (horizontally) and dy (vertically).
    pub fn inset(&self, dx: i32, dy: i32) -> Rect {
        let x = self.x + dx;
        let y = self.y + dy;
        let w = if self.width as i32 > 2 * dx { self.width as i32 - 2 * dx } else { 0 } as u32;
        let h = if self.height as i32 > 2 * dy { self.height as i32 - 2 * dy } else { 0 } as u32;
        Rect { x, y, width: w, height: h }
    }

    /// Right edge (exclusive).
    pub fn right(&self) -> i32 { self.x + self.width as i32 }
    /// Bottom edge (exclusive).
    pub fn bottom(&self) -> i32 { self.y + self.height as i32 }
}

// ── Drawing Primitives ─────────────────────────────────────────────────────

/// Draw a rounded rectangle outline.
pub fn draw_rounded_rect(
    fb: &mut Framebuffer,
    r: &Rect,
    radius: u32,
    color: Color,
) {
    let rad = core::cmp::min(radius, core::cmp::min(r.width, r.height) / 2);

    // Top edge
    fb.fill_rect(
        (r.x + rad as i32) as u32, r.y as u32,
        r.width - 2 * rad, 1,
        color,
    );
    // Bottom edge
    fb.fill_rect(
        (r.x + rad as i32) as u32, (r.bottom() - 1) as u32,
        r.width - 2 * rad, 1,
        color,
    );
    // Left edge
    fb.fill_rect(
        r.x as u32, (r.y + rad as i32) as u32,
        1, r.height - 2 * rad,
        color,
    );
    // Right edge
    fb.fill_rect(
        (r.right() - 1) as u32, (r.y + rad as i32) as u32,
        1, r.height - 2 * rad,
        color,
    );

    // Corner arcs (simplified with single pixels)
    if rad >= 1 {
        for i in 0..rad {
            let dx = (i as f64 / rad as f64) as i32;
            let dy = ((rad as f64 - (i as f64 / rad as f64)) as i32).max(0);
        }
    }
}

/// Draw a rectangle border (outline).
pub fn draw_border(fb: &mut Framebuffer, r: &Rect, width: u32, color: Color) {
    if width == 0 { return; }
    // Top
    fb.fill_rect(r.x as u32, r.y as u32, r.width, width, color);
    // Bottom
    fb.fill_rect(r.x as u32, (r.bottom() - width as i32) as u32, r.width, width, color);
    // Left
    fb.fill_rect(r.x as u32, r.y as u32, width, r.height, color);
    // Right
    fb.fill_rect((r.right() - width as i32) as u32, r.y as u32, width, r.height, color);
}

/// Draw a vertical or horizontal gradient.
pub fn draw_gradient(
    fb: &mut Framebuffer,
    r: &Rect,
    from: Color,
    to: Color,
    vertical: bool,
) {
    if vertical {
        let h = r.height;
        if h == 0 { return; }
        for y in 0..h {
            let t = y as f32 / (h - 1) as f32;
            let c = lerp_color(from, to, t);
            fb.fill_rect(r.x as u32, (r.y + y as i32) as u32, r.width, 1, c);
        }
    } else {
        let w = r.width;
        if w == 0 { return; }
        for x in 0..w {
            let t = x as f32 / (w - 1) as f32;
            let c = lerp_color(from, to, t);
            fb.fill_rect((r.x + x as i32) as u32, r.y as u32, 1, r.height, c);
        }
    }
}

fn lerp_color(a: Color, b: Color, t: f32) -> Color {
    let t = t.max(0.0).min(1.0);
    let inv = 1.0 - t;
    rgba(
        (red(a) as f32 * inv + red(b) as f32 * t) as u8,
        (green(a) as f32 * inv + green(b) as f32 * t) as u8,
        (blue(a) as f32 * inv + blue(b) as f32 * t) as u8,
        alpha(a),
    )
}

/// Draw a drop shadow under a rectangle.
pub fn draw_shadow(fb: &mut Framebuffer, r: &Rect, radius: u32, alpha: u8) {
    let blur = core::cmp::min(radius, 16u32);
    let shadow_x = r.x + 4;
    let shadow_y = r.y + 4;

    for layer in 0..blur {
        let a = alpha as u32 * (blur - layer) / blur;
        let color = rgba(0, 0, 0, a as u8);
        let inset = layer as i32;

        let sr = Rect::new(
            shadow_x + inset,
            shadow_y + inset,
            r.width.wrapping_sub((2 * inset) as u32),
            r.height.wrapping_sub((2 * inset) as u32),
        );

        fb.fill_rect(sr.x as u32, sr.y as u32, sr.width, sr.height, color);
    }
}

