// V37a — RISC-V RATS (Remote Attestation procedureS) — Attestation Service
//
// Features:
//   - Device-specific attestation signing key management
//   - Enclave attestation evidence generation (RATS-compliant format)
//   - Remote evidence verification
//   - Challenge-response protocol for freshness
//   - Supports PMP, ePMP, and Keystone-style TEE types
//
// Architecture:
//   The AttestationService holds a device-unique signing key and certificate.
//   For each enclave, it generates an AttestationEvidence structure containing
//   the enclave measurement, signer hash, TCB version, timestamp, nonce, and
//   a signature over all fields.
//
// Reference: RISC-V RATS specification, IETF RATS architecture (RFC 9334)

use crate::security::tee::{ApTeeEnclave, sha256_digest};

// ── Constants ───────────────────────────────────────────────────────────────

/// Maximum length of the attestation evidence buffer for serialization.
const EVIDENCE_BUF_SIZE: usize = 256;

/// Ed25519 signature length.
const SIGNATURE_LEN: usize = 64;

// ── TeeType ─────────────────────────────────────────────────────────────────

/// Type of TEE technology used by the platform.
#[derive(Clone, Copy, Debug, PartialEq)]
pub enum TeeType {
    /// Pure PMP isolation (V33 style).
    PMP = 0,
    /// ePMP enhanced isolation (V36d).
    EPMP = 1,
    /// Keystone-like S-mode enclave.
    Keystone = 2,
}

impl TeeType {
    pub fn name(&self) -> &'static str {
        match self {
            TeeType::PMP => "PMP",
            TeeType::EPMP => "ePMP",
            TeeType::Keystone => "Keystone",
        }
    }
}

// ── PlatformInfo ────────────────────────────────────────────────────────────

/// Platform information included in attestation evidence.
#[derive(Clone, Copy)]
pub struct PlatformInfo {
    /// CPU vendor string (e.g., "RISC-V International\0").
    pub cpu_vendor: [u8; 16],
    /// CPU model string.
    pub cpu_model: [u8; 16],
    /// Firmware version string.
    pub fw_version: [u8; 16],
    /// Type of TEE technology in use.
    pub tee_type: TeeType,
}

impl PlatformInfo {
    /// Create a new PlatformInfo with the given parameters.
    pub const fn new(
        vendor: &[u8; 16],
        model: &[u8; 16],
        fw: &[u8; 16],
        tee_type: TeeType,
    ) -> Self {
        PlatformInfo {
            cpu_vendor: *vendor,
            cpu_model: *model,
            fw_version: *fw,
            tee_type,
        }
    }

    /// Create a default PlatformInfo for QEMU virt machine.
    pub fn qemu_default(tee_type: TeeType) -> Self {
        let mut info = PlatformInfo {
            cpu_vendor: [0u8; 16],
            cpu_model: [0u8; 16],
            fw_version: [0u8; 16],
            tee_type,
        };
        info.cpu_vendor[..12].copy_from_slice(b"RISC-V Intl\0");
        info.cpu_model[..6].copy_from_slice(b"virt\0");
        info.fw_version[..8].copy_from_slice(b"AP-TEEv1\0");
        info
    }
}

// ── AttestationEvidence ─────────────────────────────────────────────────────

/// Attestation evidence structure (aligned with RATS specification).
///
/// This evidence is generated by the TEE for a specific enclave and
/// can be verified by a remote verifier.
#[derive(Clone, Copy)]
pub struct AttestationEvidence {
    /// SHA-512 measurement of the enclave (64 bytes).
    pub enclave_measurement: [u8; 64],
    /// SHA-256 hash of the enclave signing key (32 bytes).
    pub signer_hash: [u8; 32],
    /// TCB version of the TEE (u32).
    pub tcb_version: u32,
    /// Timestamp of evidence generation (tick count).
    pub timestamp: u64,
    /// Nonce for freshness (anti-replay).
    pub nonce: [u8; 16],
    /// Ed25519 signature over the evidence fields (64 bytes).
    pub signature: [u8; 64],
    /// Platform information.
    pub platform_info: PlatformInfo,
}

impl AttestationEvidence {
    /// Serialize the evidence into a byte buffer for transmission.
    ///
    /// Format:
    ///   [0..64)   — enclave_measurement
    ///   [64..96)  — signer_hash
    ///   [96..100) — tcb_version (4 bytes LE)
    ///   [100..108) — timestamp (8 bytes LE)
    ///   [108..124) — nonce
    ///   [124..188) — signature
    ///   [188..232) — platform_info (cpu_vendor + cpu_model + fw_version + tee_type)
    ///   Total: 236 bytes
    pub fn serialize(&self, buf: &mut [u8]) -> usize {
        let total = 236usize;
        if buf.len() < total {
            return 0;
        }

        let mut pos = 0;

        // enclave_measurement (64 bytes)
        buf[pos..pos + 64].copy_from_slice(&self.enclave_measurement);
        pos += 64;

        // signer_hash (32 bytes)
        buf[pos..pos + 32].copy_from_slice(&self.signer_hash);
        pos += 32;

        // tcb_version (4 bytes LE)
        buf[pos..pos + 4].copy_from_slice(&self.tcb_version.to_le_bytes());
        pos += 4;

        // timestamp (8 bytes LE)
        buf[pos..pos + 8].copy_from_slice(&self.timestamp.to_le_bytes());
        pos += 8;

        // nonce (16 bytes)
        buf[pos..pos + 16].copy_from_slice(&self.nonce);
        pos += 16;

        // signature (64 bytes)
        buf[pos..pos + 64].copy_from_slice(&self.signature);
        pos += 64;

        // platform_info: cpu_vendor (16) + cpu_model (16) + fw_version (16) + tee_type (4)
        buf[pos..pos + 16].copy_from_slice(&self.platform_info.cpu_vendor);
        pos += 16;
        buf[pos..pos + 16].copy_from_slice(&self.platform_info.cpu_model);
        pos += 16;
        buf[pos..pos + 16].copy_from_slice(&self.platform_info.fw_version);
        pos += 16;
        buf[pos..pos + 4].copy_from_slice(&(self.platform_info.tee_type as u32).to_le_bytes());
        pos += 4;

        pos
    }

    /// Deserialize evidence from a byte buffer.
    pub fn deserialize(buf: &[u8]) -> Option<Self> {
        if buf.len() < 236 {
            return None;
        }

        let mut evidence = AttestationEvidence {
            enclave_measurement: [0u8; 64],
            signer_hash: [0u8; 32],
            tcb_version: 0,
            timestamp: 0,
            nonce: [0u8; 16],
            signature: [0u8; 64],
            platform_info: PlatformInfo::qemu_default(TeeType::PMP),
        };

        let mut pos = 0;

        evidence.enclave_measurement.copy_from_slice(&buf[pos..pos + 64]);
        pos += 64;

        evidence.signer_hash.copy_from_slice(&buf[pos..pos + 32]);
        pos += 32;

        evidence.tcb_version = u32::from_le_bytes(buf[pos..pos + 4].try_into().ok()?);
        pos += 4;

        evidence.timestamp = u64::from_le_bytes(buf[pos..pos + 8].try_into().ok()?);
        pos += 8;

        evidence.nonce.copy_from_slice(&buf[pos..pos + 16]);
        pos += 16;

        evidence.signature.copy_from_slice(&buf[pos..pos + 64]);
        pos += 64;

        evidence.platform_info.cpu_vendor.copy_from_slice(&buf[pos..pos + 16]);
        pos += 16;
        evidence.platform_info.cpu_model.copy_from_slice(&buf[pos..pos + 16]);
        pos += 16;
        evidence.platform_info.fw_version.copy_from_slice(&buf[pos..pos + 16]);
        pos += 16;

        let tee_type_val = u32::from_le_bytes(buf[pos..pos + 4].try_into().ok()?);
        evidence.platform_info.tee_type = match tee_type_val {
            0 => TeeType::PMP,
            1 => TeeType::EPMP,
            2 => TeeType::Keystone,
            _ => return None,
        };

        Some(evidence)
    }
}

// ── AttestationResponse ─────────────────────────────────────────────────────

/// Response to a challenge (for challenge-response protocol).
pub struct AttestationResponse {
    /// The challenge nonce this responds to.
    pub challenge: [u8; 16],
    /// Fresh attestation evidence.
    pub evidence: AttestationEvidence,
    /// Additional endorsement data (e.g., TEE properties).
    pub endorsement: [u8; 64],
}

impl AttestationResponse {
    /// Serialize the response into a byte buffer.
    pub fn serialize(&self, buf: &mut [u8]) -> usize {
        let mut pos = 0usize;

        // challenge (16 bytes)
        if pos + 16 > buf.len() { return pos; }
        buf[pos..pos + 16].copy_from_slice(&self.challenge);
        pos += 16;

        // evidence (using serialize)
        let mut evidence_buf = [0u8; 256];
        let e_len = self.evidence.serialize(&mut evidence_buf);
        if pos + 4 + e_len > buf.len() { return pos - 16; }
        buf[pos..pos + 4].copy_from_slice(&(e_len as u32).to_le_bytes());
        pos += 4;
        buf[pos..pos + e_len].copy_from_slice(&evidence_buf[..e_len]);
        pos += e_len;

        // endorsement (64 bytes)
        if pos + 64 > buf.len() { return pos; }
        buf[pos..pos + 64].copy_from_slice(&self.endorsement);
        pos += 64;

        pos
    }
}

